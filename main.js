/* Math.max() & Math.min()

El método Math.max() devuelve el número con el valor más alto.

El método Math.min() devuelve el número con el valor más bajo.
*/
let x = Math.max(5, 10); // regresa el 10

let y = Math.min(5, 10); // regresa el 5

let a = Math.max(20, -50); //regresa el 20
let b = Math.max(0, 150, 30, 20, 38); //regresa el 150
let c = Math.max(-5, 10); //regresa el 10
let d = Math.max(-5, -10); //regresa el -5
let e = Math.max(1.5, 2.5); //regresa 2.5

let aa = Math.min(20, -50); //regresa el -50
let bb = Math.min(0, 150, 30, 20, 38);//regresa el 0
let cc = Math.min(-5, 10); //regresa el -5
let dd = Math.min(-5, -10);//regresa el -10
let ee = Math.min(1.5, 2.5); //regresa el 2.5